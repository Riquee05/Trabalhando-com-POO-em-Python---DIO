# Encapsulamento em Python é a prática de restringir o acesso direto aos atributos e métodos de um objeto para proteger e controlar a modificação de seu estado interno.

# ou seja, encapsulamento em Python significa proteger os dados de um objeto para que possam ser acessados e modificados apenas por meio de métodos definidos, 
#garantindo controle e segurança sobre o estado interno do objeto.

