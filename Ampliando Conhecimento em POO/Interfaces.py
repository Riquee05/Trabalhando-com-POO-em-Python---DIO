# Conceito de Interface.

# Uma interface define o que uma classe Deve fazer e não como.

# O conceito de Interface é definir um contrato, onde são declarados os metodos(O que deve ser Feito),
# e suas respectivas assinaturas, em Python ultilizamos classes Abstratas para criar contratos.
# Classes abstratas não podem ser instanciadas

# Python permite Herança multipla, ou seja, pode extender quantas classes quiser

# Em programação, um contrato é um conjunto de regras e expectativas definidas entre diferentes partes de um sistema, 
# especificando as responsabilidades e obrigações de cada parte, 
# como métodos a serem implementados, tipos de retorno e condições de entrada e saída, 
# para garantir uma interação correta e consistente.